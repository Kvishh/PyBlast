def create_tiles():
    for x, row in enumerate(tile_map):
        for y, column in enumerate(row):
            if column == 1:
                tiles.append(Tile(y*TILE_SIZE, x*TILE_SIZE))

def draw_tiles(scroll):
    for tile in tiles:
        display.blit(tile.image, (tile.rect.x-scroll[0], tile.rect.y-scroll[1]))

def load_bg_images():
    for i in range(1, 4):
        image = pygame.image.load(f"assets/images/bg_{i}.png").convert_alpha()
        bg_images.append(image)

def draw_background(scroll):
    speed = .2
    for bg_image in bg_images:
        display.blit(bg_image, (0 - scroll[0] * speed, 0-scroll[1]))
        speed+=.2

def load_long_rocks():
    i = 1
    while i < 5:
        if i == 3:
            long_rock = pygame.image.load("assets/images/long_rock1.png").convert_alpha()
            long_rocks.append(long_rock)
            i+=1
            continue
        elif i == 4:
            long_rock = pygame.transform.rotate(pygame.transform.flip(pygame.image.load("assets/images/long_rock2.png").convert_alpha(), True, False), 10)
            long_rocks.append(long_rock)
            i+=1
            continue

        long_rock = pygame.image.load(f"assets/images/long_rock{i}.png").convert_alpha()
        long_rocks.append(long_rock)
        i+=1

def draw_behind_long_rocks(scroll):
    long_rocks[1].set_alpha(150)
    long_rocks[2].set_alpha(150)
    display.blit(long_rocks[1], (0-scroll[0], 200-scroll[1]))
    display.blit(long_rocks[2], ((WINDOW_WIDTH - long_rocks[2].width + 150) - scroll[0], 0 - scroll[1]))

def draw_front_long_rocks(scroll):
    long_rocks[0].set_alpha(200)
    long_rocks[3].set_alpha(200)
    display.blit(long_rocks[0], (-100 - scroll[0], 0 - scroll[1]))
    display.blit(long_rocks[3], ((WINDOW_WIDTH - long_rocks[3].width+300) - scroll[0], 200 - scroll[1]))